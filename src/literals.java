import java.util.*;

public class literals {
    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        int a = 50;
        byte b = 43;
        float f = 43.5f;
        double d = 54.87;
        long l = 4555566666L;
        char c = 'P';
        String cha = "Parth";
        System.out.println(a + " " + b + " " + f + " " + d + " " + l + " " + c + " ");
        System.out.println(cha);
        int xd = s.nextInt();
        System.out.println("Entered Value = " + xd);

    }
}
